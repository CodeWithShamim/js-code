const namesArray = ['shamim', 'salman', 'shahinn islam', 'hasmot', 'jubaidul', 'shamim islam'];
const result = bestFriend(namesArray);
console.log(result);
function bestFriend(names){
    let largeName = "";
    for (let name of names){
        if (name.length>largeName.length){
            largeName = name;
        }
    }
    return largeName;

};


